const express = require('express');
const path = require('path');
const logger = require('morgan');
const cookieParser = require('cookie-parser');
const bodyParser = require('body-parser');
const mongoose = require('mongoose');
const index = require('./routes/index');
const admin = require('./routes/admin');
const jwt = require('jsonwebtoken');
const keyConfig = require('./config').default;

const app = express();

app.jwt = jwt;
app.jwtSecret = 'dga-vote-chain';

// view engine setup
app.set('views', path.join(__dirname, 'views'));
app.set('view engine', 'ejs');

app.use(logger('dev'));
app.use(bodyParser.json());
app.use(bodyParser.urlencoded({
  extended: false
}));
app.use(cookieParser());
app.use(require(__dirname + '/middleware.js').makeAuthHappen().unless({
  path: ['/404']
}));
app.use(express.static(path.join(__dirname, 'public')));

app.use('/', index);
app.use('/admin', admin);

// catch 404 and forward to error handler
app.use(function (req, res, next) {
  const err = new Error('Not Found');
  err.status = 404;
  next(err);
});

// error handler
app.use(function (err, req, res, next) {
  console.error(err);
  
  // set locals, only providing error in development
  res.locals.message = err.message;
  res.locals.error = process.env.NODE_ENV === 'development' ? err : {};

  // render the error page
  res.status(err.status || 500);
  res.render('error', {
		JWTData: req.JWTData
	});
});

/**************************************MongoDB Database***************************************/
const mongoURI = keyConfig.mongoURI;

app.db = mongoose.createConnection(mongoURI);

app.db.on('error', console.error.bind(console, 'mongoose connection error: '));

app.db.once('open', function () {
  console.log("Connected to ", mongoURI);
});

import { models } from './model';
models(app, mongoose);

